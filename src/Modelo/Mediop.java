
package Modelo;


public class Mediop {
    private int id;
    private String pago;

    public Mediop() {
    }

    public Mediop(int id, String pago) {
        this.id = id;
        this.pago = pago;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPago() {
        return pago;
    }

    public void setPago(String pago) {
        this.pago = pago;
    }
    
    
}
